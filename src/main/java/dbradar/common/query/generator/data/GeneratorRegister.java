    private GeneratorRegister(GlobalState globalState) {
        initBasicGenerators();
        initComposeGenerators(globalState.getGeneratorConfigPath());
    }

    public static Generator getGenerator(AbstractTableColumn<?, ?> column, GlobalState globalState) {
        if (register == null) {
            register = new GeneratorRegister(globalState);
        }
        return register.getGeneratorInternal(column);
    }

    private Generator getGeneratorInternal(AbstractTableColumn<?, ?> column) {
        Generator generator;
        String type = column.getType().toString().toLowerCase();
        generator = composeGenerators.get(type);
        generator = (generator == null) ? basicGenerators.get(type) : generator;

        if (Objects.equals(type, "none")) {
            // According to the sqlancer code, the value type generated by the none type
            // data column is TEXT.
            generator = basicGenerators.get("text");
        }

        if (generator == null) {
            // TODO: the column type may be none(t1.c1:NONE), resulting in the error
            // Generator Register: cannot find the corresponding data generator for none
            throw new RuntimeException("Generator Register: cannot find the corresponding data generator for " + type);
        }

        return generator;
    }

    public static Map<String, Generator> getBasicGenerators(GlobalState globalState) {
        if (register == null) {
            register = new GeneratorRegister(globalState);
        }
        return register.basicGenerators;
    }

