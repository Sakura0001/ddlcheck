    private void initComposeGenerators(String defaultConfigPath) {
        composeGenerators = new HashMap<>();
        DBConfig.initDBConfig(defaultConfigPath);
        DBConfig dbConfig = DBConfig.getDBConfig();
        Map<String, List<String>> dataGeneratorConfig = dbConfig.getDataConfig();

        for (String dataType : dataGeneratorConfig.keySet()) {
            List<String> generatorOptions = dataGeneratorConfig.get(dataType);

            List<Generator> generators = new ArrayList<>();
            for (String option : generatorOptions) {
                Generator generator = basicGenerators.get(option);
                if (generator == null) {
                    generator = new ConstGenerator(option);
                }
                generators.add(generator);
            }
            composeGenerators.put(dataType.toLowerCase(), new ComposeGenerator(generators));
        }
    }

